    # Original key
    variations.append(base_key)
    
    # If key contains our marker, focus on the surrounding context
    if marker in base_key:
        marker_pos = base_key.find(marker)
        
        # Get context before and after marker
        before_marker = base_key[:marker_pos]
        after_marker = base_key[marker_pos + len(marker):]
        
        # Add variations with different context lengths
        if len(before_marker) >= 32:
            variations.append(before_marker[-32:] + marker)
        if len(after_marker) >= 32:
            variations.append(marker + after_marker[:32])
        
