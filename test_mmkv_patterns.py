def try_decrypt_with_key(db_path, key, config_path=None):
    """Try to decrypt the database with a given key."""
    try:
        if config_path:
            # Use existing SQLCipher config
            cmd = f'sqlcipher "{db_path}" -init "{config_path}" ".tables"'
        else:
            # Create temporary config
            with tempfile.NamedTemporaryFile(mode='w', delete=False) as f:
                f.write(f'PRAGMA key = "{key}";\n')
                f.write('PRAGMA cipher_compatibility = 3;\n')
                config_path = f.name
            cmd = f'sqlcipher "{db_path}" -init "{config_path}" ".tables"'
        
        result = subprocess.run(cmd, shell=True, capture_output=True, text=True)
        if "Error" not in result.stderr and result.returncode == 0:
            print(f"Potential valid key found: {key}")
            print("Tables:", result.stdout)
            return True
        return False
    except Exception as e:
        print(f"Error testing key {key}: {str(e)}")
        return False
    finally:
        if config_path and os.path.exists(config_path):
            try:
                os.unlink(config_path)
            except:
                pass

